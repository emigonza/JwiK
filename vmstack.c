static tVmStack   *stack;     // the pysical base of the whole stack (incl. statics)
static tVmStack   *sp;        // the current stack pointer
static tVmStack   *stackbase; // the base of the runtime stack (excl. statics)

// *******************************************************************************************************************
// Public API

// void StackInitialize(uint8_t staticFields)
//
// Arguments:
//	uint8_t staticFields - Count of static fields
//
// Returns:
//  void - Nothing
void StackInitialize(uint8_t staticFields) 
{
	// the stack is generated by stealing from the heap. This
	// is possible since the class file tells us how many stack
	// elements the call to a method requires
	stack = (tVmStack  *)GetHeapBase();
	sp = stack-1;

	// steal one item for mains args and the space required for 
	// the static fields
	HeapSteal((1+staticFields)*sizeof(tVmStack)); 

	// increase stack pointer behind static fields
	sp += staticFields;
}

// void StackPush(tVmStack val)
//
// Arguments:
//	tVmStack val - value to push
//
// Returns:
//  void - Nothing
void StackPush(tVmStack val) 
{
	sp++;
	*sp=val;
	
//  	*(++sp) = val;
}

// tVmStack StackPop(void)
//
// Arguments:
//  nothing
//
// Returns:
//  tVmStack - value from stack
tVmStack StackPop(void) 
{
  	return *(sp--);
}
// int StackPopInt(void)
//
// Arguments:
//  nothing
//
// Returns:
//  tVmInt - value from stack
tVmInt StackPopInt(void) 
{
  	return VmStackToInt(*(sp--));
}

// tVmStack StackPeep(uint8_t index)
