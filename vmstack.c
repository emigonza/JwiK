// void *StackAddToSp(int8_t offset)
//
// Arguments:
//  int8_t offset - offset to add to stack pointer
//
// Returns:
//  void nothing
void StackAddToSp(int8_t offset) 
{
  	sp += offset;
}
// tVmStack   * GetStackPointer(void) 
//
// Arguments:
//  none
//
// Returns:
//  tVmStack - stack pointer
tVmStack   * GetStackPointer(void) 
{
  	return sp;
}

